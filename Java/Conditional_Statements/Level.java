class Level
{
	public static void main(String []args)
	{
		int level=3;
		switch(level)
		{
			case 1:
				System.out.println("Your Level is Beginner");
				break;
			case 2:
				System.out.println("Your Level is Indermediate");
				break;
			case 3:
				System.out.println("Your Level is Expert");
				break;
			default:
				System.out.println("Oops..!");

		}
	}
}
